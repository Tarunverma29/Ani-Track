export function AnsiArt({ variant = "deer", className }: { variant?: "deer" | "face" | "history" | "settings"; className?: string }) {
  const variants = { deer, face, history, settings };
  const art = variants[variant];
  const fontSize = variant === "face" ? "0.3rem" : variant === "history" ? "0.35rem" : "0.5rem";
  return (
    <div className={className} style={{ overflow: "hidden" }}>
      <pre
        style={{
          fontFamily: "'JetBrains Mono', monospace",
          fontSize,
          lineHeight: 1.05,
          color: "#b83040",
          margin: 0,
          padding: 0,
          letterSpacing: 0,
          userSelect: "none",
          overflow: "hidden",
        }}
      >
        {art.join("\n")}
      </pre>
    </div>
  );
}
